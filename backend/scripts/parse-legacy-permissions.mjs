/**
 * Parses tools/legacy_permissions_raw.txt (hierarchy + user-count lines)
 * and writes src/modules/access/legacy-permissions.generated.ts
 */
import { createHash } from "node:crypto";
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const rawPath = join(root, "tools", "legacy_permissions_raw.txt");

const ROOTS = new Set([
  "Дашбоард",
  "Заказы",
  "Клиенты",
  "Накладные",
  "Кассы",
  "Склады",
  "Поставщики",
  "Планы",
  "Пользователи",
  "GPS",
  "Настройки"
]);

const ROOT_MODULE_EN = {
  Дашбоард: "dashboard",
  Заказы: "orders",
  Клиенты: "clients",
  Накладные: "invoices",
  Кассы: "cash",
  Склады: "warehouse",
  Поставщики: "suppliers",
  Планы: "plans",
  Пользователи: "staff",
  GPS: "gps",
  Настройки: "settings"
};

/** GOST-like ASCII for permission key slugs */
const CYR_TO_LAT = {
  а: "a",
  б: "b",
  в: "v",
  г: "g",
  д: "d",
  е: "e",
  ё: "yo",
  ж: "zh",
  з: "z",
  и: "i",
  й: "y",
  к: "k",
  л: "l",
  м: "m",
  н: "n",
  о: "o",
  п: "p",
  р: "r",
  с: "s",
  т: "t",
  у: "u",
  ф: "f",
  х: "h",
  ц: "ts",
  ч: "ch",
  ш: "sh",
  щ: "shch",
  ъ: "",
  ы: "y",
  ь: "",
  э: "e",
  ю: "yu",
  я: "ya"
};

function translit(s) {
  let out = "";
  for (const ch of s) {
    const lat = CYR_TO_LAT[ch.toLowerCase()];
    out += lat !== undefined ? lat : ch;
  }
  return out;
}

function slugPart(s) {
  return translit(s)
    .normalize("NFKD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[^A-Za-z0-9]+/g, "_")
    .replace(/_+/g, "_")
    .replace(/^_|_$/g, "")
    .toLowerCase();
}

/** Legacy doc always uses "<N> пользовател..." for attachment counts (ё/е variants). */
function isCountLine(line) {
  return /^\d+\s+пользовател/i.test(line.trim());
}

function parseLeaves(lines) {
  const stack = [];
  let pendingAfterLeaf = false;
  const leaves = [];

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    if (!line || isCountLine(line)) continue;

    if (ROOTS.has(line)) {
      stack.length = 0;
      stack.push(line);
      pendingAfterLeaf = false;
      continue;
    }

    const next = lines[i + 1];
    const nextIsCount = next !== undefined && isCountLine(next);

    if (nextIsCount) {
      const path = [...stack, line];
      leaves.push({ path, title: line });
      pendingAfterLeaf = true;
      continue;
    }

    if (pendingAfterLeaf) {
      if (stack.length > 1) stack.pop();
      pendingAfterLeaf = false;
    }

    const dupSkip =
      stack.length > 0 &&
      line === stack[stack.length - 1] &&
      !(line === "Кассы" && stack.length === 1);
    if (dupSkip) continue;

    stack.push(line);
  }

  return leaves;
}

function buildKey(path) {
  const rootRu = path[0];
  const rootEn = ROOT_MODULE_EN[rootRu] ?? "misc";
  const tail = path.slice(1).map(slugPart).filter(Boolean);
  const base = [rootEn, ...tail].join(".");
  let key = base.slice(0, 180);
  if (key.length === 180) {
    const h = createHash("sha1").update(base).digest("hex").slice(0, 8);
    key = `${base.slice(0, 170)}_${h}`;
  }
  return key;
}

function main() {
  const raw = readFileSync(rawPath, "utf8");
  const lines = raw.split(/\r?\n/).map((l) => l.trimEnd());
  const leaves = parseLeaves(lines);

  const used = new Map();
  const entries = [];
  for (const { path, title } of leaves) {
    let key = buildKey(path);
    const n = (used.get(key) ?? 0) + 1;
    used.set(key, n);
    if (n > 1) {
      const suffix = `_${n}`;
      key = (key.slice(0, 180 - suffix.length) + suffix).slice(0, 180);
    }
    const section = path.slice(0, -1).join(" / ").slice(0, 120) || null;
    const description = path.join(" / ");
    entries.push({ key, description, section });
  }

  const linesOut = [
    "/** AUTO-GENERATED by scripts/parse-legacy-permissions.mjs — do not edit by hand. */",
    "export const LEGACY_PERMISSION_METADATA: Record<string, { description: string; section?: string }> = {"
  ];
  for (const e of entries) {
    const desc = JSON.stringify(e.description);
    const sec = e.section == null ? "" : `, section: ${JSON.stringify(e.section)}`;
    linesOut.push(`  ${JSON.stringify(e.key)}: { description: ${desc}${sec} },`);
  }
  linesOut.push("};");

  const outPath = join(root, "src", "modules", "access", "legacy-permissions.generated.ts");
  writeFileSync(outPath, linesOut.join("\n") + "\n", "utf8");
  console.log(`Wrote ${entries.length} permissions to ${outPath}`);
}

main();
